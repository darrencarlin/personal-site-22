import Blurb from "components/Blurb";
import Education from "components/Education";
import RecentPosts from "components/RecentPosts";
import Work from "components/Work";
import type { GetStaticProps } from "next";
import Head from "next/head";
import {
  AboutItemProps,
  BlurbItemProps,
  EducationItemProps,
  RecentPostItemProps,
  RecentPostsProps,
  WorkItemProps,
} from "types/components";
import {
  getAbout,
  getBlurb,
  getEducation,
  getRecentPosts,
  getWork,
} from "utils/contentful";

export interface HomePageProps {
  blurb: BlurbItemProps;
  education: EducationItemProps[];
  work: WorkItemProps[];
  about: AboutItemProps;
  recent: RecentPostItemProps[];
}

const Home = ({ blurb, education, work, about, recent }: HomePageProps) => {
  return (
    <>
      <Head>
        <title>Darren Carlin | Home</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      {/* <About about={about} /> */}
      <Blurb blurb={blurb} />
      <Work work={work} />
      <Education education={education} />
      <RecentPosts recent={recent} />
    </>
  );
};

export default Home;

export const getStaticProps: GetStaticProps = async () => {
  const blurb = await getBlurb();
  const about = await getAbout();
  const education = await getEducation();
  const work = await getWork();
  const recent = await getRecentPosts();
  return { props: { blurb, education, work, about, recent } };
};
